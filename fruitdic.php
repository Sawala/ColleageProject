<p class="subtitle">前言</p>
  <p align="left" style="line-height: 180%;">　　印度棗俗稱棗、棗仔或蜜棗，盛產期正值新舊曆過年期間，又有”團員果”之稱號；產區集中於高雄市、屏東縣、臺南市及嘉義縣等地。
印度棗實生苗幼年期短，播種後第二年可結果，質量性狀可於早期評估；加上芽條變異頻繁，育種年限較短；日據時代至今，農民利用實生選種及芽條變異選種育成超過35個栽培品種；且因產區集中，果農交換資訊方便，因此品種更替速度極快，例如民國80年以後主要栽培品種：高朗一號、高朗二號、蜜棗、仙桃、金桃、中葉等均由農民育成。</p>
  <p class="subtitle">育種過程</p>
  <p align="left" style="line-height: 180%;">　　本場自92年起至98年間進行新品種之育種，由天然授粉之黃冠實生苗族群中選育出3個優良品系，再經過品系比較試驗後，選出具有晚熟、總可溶性固形物高、果肉細緻、櫥架壽命長、以及官能品評佳等優良特性之KIS-95590新品系，經過品種權審議後，命名登記為高雄10號玉寶。蜜棗使用嫁接技術，優良、甜蜜多汁的蜜棗，擷取其果樹的枝與芽，與其他果樹下部的枝幹結合在一起，可增加其適應力，並改良品種。</p>
